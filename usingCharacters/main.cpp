#include <iostream>

using namespace std;

/* when using a character which is a single letter,digit or symbol you have to use the char prefix when declaring your variable.*/
int main()
{
    char ch; /* used char to declare variable "ch"*/
    ch = 'a'; /* assigned a value of 'a' to ch -- BE VERY CAREFUL you have to USE SINGLE QUOTES to assign a value to a variable that is holding a character!*/
    cout << ch << endl;
}
